	<div class="flex-item-cards flow-text">
		<div class="text-center-h"><b>Building Programmes</b></div>
		<div class="text-justify">We finance the construction and repair of Orphanages, Hospitals and Care Centres. We support regional economies by purchasing all building materials locally, and in turn, providing employment to the local workforce.</div>
	</div>

	<div class="flex-item-cards flow-text">
		<div class="text-center-h"><b>Medical / Care Programme</b></div>
		<div class="text-justify">We are activley involved in Hospice work for the elderly. We provide overseas medical treatment for sick children and actively participate in the Barretstown Programme. Our Rescue and Rehabilitation programme provides families for homeless children enabling them to ultimately make a valuable contribution to society.</div>
	</div>

	<div class="flex-item-cards flow-text">
		<div class="text-center-h"><b>Education Programme</b></div>
		<div class="text-justify">We run an education programme for schools and colleges in which we present the plight of the people of Kazakhstan and the Underground Children of Kiev, educating students about the wider world and the suffering that exists there. It incorporates a documentary showing about our work. Along with this we provide a photographic presentation, lecture and question and answer session. We also offer students the opportunity of gaining work experience at our offices and aid centre in Cork.</div>
	</div>
</div>

