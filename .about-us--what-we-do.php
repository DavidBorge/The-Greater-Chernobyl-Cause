		<div class="text-center-h"><b>Humanitarian Aid</b></div>
		<div class="text-justify">We send 40ft containers of Humanitarian Aid to Kazakhstan, Ukraine & Russia biannually. In addition, we operate a programme for overseas volunteers. Click here for more information on volunteering. By sending aid in sealed containers by sea we ensure that your donations reach those in dire need.</div>
