<p class="flow-text bolder text-center-h">
	The Greater Chernobyl Cause is based in Cork, Ireland.
</p>

<p class="flow-text text-justify">
	The charity's head, <b>Fiona Corcoran</b>, was deeply moved when the worlds worst nuclear disaster occurred at <b>Chernobyl</b> in <b>Ukraine in 1986</b>. An enthusiastic follower of the Campaign for Nuclear Disarmament, she was shocked and horrified by the consequences of the explosion so much so, that she eventually gave up her job to work full-time with the victims of the poisonous radioactive fallout.
</p>
<p class="flow-text text-justify">
	The Greater Chernobyl Cause began as a <b>response to the world's worst nuclear disaster</b> but has now widened its mission to concentrate on the <b>human casualties</b> of the break-up of the former Soviet Union.Nowhere more so than in Kazakhstan where those who survived early life in a city orphanage faced the horrific prospect of being transferred hundreds of miles to a squalid and disease-ridden institution in Ayagus. For some, it amounted to a sentence of death.
</p>
<p class="flow-text text-justify">
	Kazakhstan is the largest landlocked country in the world. The similarities with the Chernobyl disaster are startling. It was here that the Soviet Union exploded 500 nuclear weapons in a hand-picked test site over a period of 40 years.
</p>
<p class="flow-text text-justify">
	The results of the charity’s efforts are equally startling – new and refurbished orphanages and hospices, vital domestic appliances and medical equipment that most of us take for granted, shelter for vulnerable street children and for some, even the possibility of adoption.
</p>
<p class="flow-text text-justify">
	Under the overall management of a Board of Directors with Fiona Corcoran at the helm, the charity draws <b>volunteers</b> from every sector of <b>Irish life</b>, from the Cork Fire Brigade and local businesses to enthusiastic <b>schoolchildren</b>. However, such is the scale of the challenge that <b>the charity is always looking for new supporters</b>.
</p>


