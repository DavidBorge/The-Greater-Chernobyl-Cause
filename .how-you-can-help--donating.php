	<p class="flow-text text-justify">
		<span class="text-bold">How does charity tax relief work?</span><br>
		If you are a PAYE only tax payer, and have given €250 or more to the Greater Chernobyl Cause in the course of a tax year, the Cause can reclaim the tax paid on your donations. This only applies to personal donations you have made and not to money raised through sponsorship.
	</p>
	<p class="flow-text text-justify">
		<span class="text-bold">What do you have to do?</span><br>
		Very little! The Greater Chernobyl Cause mails a tax relief form to all individual donors whose donations qualify for a tax reclaim. Simply complete the form and return it to the Cause.<br>
		If you are self-assessed simply send your donation receipt with your annual tax return form as proof of payment, to the Revenue Commissioners.<br>
		If you are a company, treat your donation(s) as normal business expense, deductible at the appropriate rate of corporation tax.
	</p>
	<p class="flow-text text-justify">
		<span class="text-bold">What Donations Qualify for Relief?</span><br>
		The minimum donation in any single year that must be made to any one eligible.
	</p>
	<p class="flow-text text-justify">
		<span class="text-bold">Example 1 - PAYE-ONLY Individual</span><br>
		<ol type="a" class="flow-text text-justify">
			<li>
				Individual on the standard rate of tax, i.e. 20%.<br>
				Individual donates €250<br>
				Value of donation to The Greater Chernobyl Cause = €312.50<br>
				The Greater Chernobyl Cause is able to claim a repayment of €62.50 from Revenue at the end of the tax year.
			</li>
			<li>
				Individual on the standard rate of tax, i.e. 20%.<br>
				Individual donates €500<br>
				Value of donation to the Greater Chernobyl Cause = €625<br>
				The Greater Chernobyl Cause is able to claim a repayment of €125 from Revenue at the end of the tax year.
			</li>
			<li>
				Individual on the higher rate of tax, i.e. 41%.<br>
				Individual donates €250<br>
				Value of donation to the Greater Chernobyl Cause = €423.73<br>
				The Greater Chernobyl Cause is able to claim a repayment of €173.73 from Revenue at the end of the tax year.
			</li>
			<li>
				Individual on the higher rate of tax, i.e. 41%.<br>
				Individual donates €500<br>
				Value of donation to the Greater Chernobyl Cause = €847.46<br>
				The Greater Chernobyl Cause is able to claim a repayment of €347.46 from Revenue at the end of the tax year.
				The individual PAYE-only donor must complete an ‘Appropriate Certificate’ and forward it to the relevant eligible charity or approved body, as the case may be, to allow it to claim the grossed up amount of tax associated with the donation.
			</li>
		</ol>
	</p>
	<p class="flow-text text-justify">
		<span class="text-bold">Example 2 - Self-Assessed Individual</span><br>
		<ol type="a" class="flow-text text-justify">
			<li>
				Individual on the standard rate, i.e. 20%.<br>
				Individual donates €500 and receives tax relief @ 20% (€100).<br>Cost to the taxpayer is €400 and receipt by eligible charity or approved body is €500 - the self-assessed individual will simply claim a deduction for the donation on his/her tax return, there is no grossing up arrangement and therefore no repayment claim by the eligible charity or approved body arises.
			</li>
			<li>
				Individual on the higher rate, i.e. 41%.<br>
				Individual donates €500 and receives tax relief @ 41% (€205).<br>
				Cost to the taxpayer is €295 and receipt by eligible charity or approved body is €500 - again there is no grossing up arrangement and therefore no repayment claim by the eligible charity or approved body arises.
			</li>
		</ol>
