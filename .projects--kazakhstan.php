<div class="project-card z-depth-2">
	<div class="project-title flow-text bolder text-center-h text-white red darken-2">
		Semipalatinsk Hospice
	</div>
	<amp-vimeo data-videoid="17743031" width="640" height="480" layout="responsive" class="video-4x3"></amp-vimeo>
	<p class="flow-text text-justify">The hospice in the city Semipalatinsk of has come a long way since 2008 to the opening of the new hospice building in September 2016. Semey is a city in Kazakhstan in the East Kazakhstan region in the Kazakhstan part of Siberia near the border with Russia. The “hospice from hell” in 2008 saw elderly skeletal figures living in squalor existing on a very poor diet in an area “where temperatures plummet to minus 40 degrees Celsius in winter”. People here were subsisting on just four cent per day, which allowed them a diet consisting mainly of watery porridge, which was coupled with the absence of even basic hygiene.</p>
	<p class="flow-text text-justify">Immediate improvements were made to the hospice while mounting an appeal for funds for a new building. Semipalatinsk is in a region that has long been contaminated with radioactive fallout from the Soviet Union’s 40-year nuclear testing programme. This part of Kazakhstan remains polluted with radioactive materials and the population continues to suffer from this silent menace and grinding poverty. The regional government gave some free land and while construction began for an entirely new hospice, the charity set a target for quarter of a million euro for this. The site was on the grounds of the general hospital for the area so that people could be medically treated by transferring from the hospital on site. With help from sponsors, Fiona made seven site visits with numerous appointments and phone calls along the way. Air Astana made much of this possible by coming on board as a sponsor.</p>
	<p class="last-paragraph flow-text text-justify">The East Kazakh government have agreed to take over the running of the hospital, provide and pay for the staff bringing economic as well as social benefit for the city. Residents are overwhelmed by the change. They have been given “dignity and a sense of worth and well-being in the final years of their lives.’</p>
</div>

<div class="project-card z-depth-2">
	<div class="project-title flow-text bolder text-center-h text-white red darken-2">
		Jeremy Irons Appeal
	</div>
	<amp-vimeo data-videoid="13522589" width="640" height="480" layout="responsive" class="video-4x3"></amp-vimeo>
	<p class="flow-text text-justify">Many of the 13 million who live in this vast country are poor and deprived of the services that we take for granted in the west. Sadly, under funded and ill equipped orphanages are still all too common. 17,000 orphans are confined to grim institutions. The majority have been abandoned by parents, unable - either through poverty or neglect - to look after their needs.</p>
	<p class="last-paragraph flow-text text-justify">Actor Jeremy Irons is one of our greatest supporters and is appealing for your help.</p>
</div>

<div class="project-card z-depth-2">
	<div class="project-title flow-text bolder text-center-h text-white red darken-2">
		Znamenka and Berik
	</div>
	<amp-vimeo data-videoid="13519127" width="640" height="480" layout="responsive" class="video-4x3"></amp-vimeo>
	<p class="last-paragraph flow-text text-justify">Sadly, some of the children are victims of a Soviet legacy that is continuing to poison the land and its people. For it was in East Kazakhstan that one third of the world’s nuclear tests took place in total secrecy. There were as many as 500 explosions in 40 years and some mothers exposed to radiation fallout wonder about the long term effects for their families. Take a look at the situation in one village close to the test site.</p>
</div>

<div class="project-card z-depth-2">
