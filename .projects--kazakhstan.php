	<p class="flow-text text-justify">The Director of the charity, Fiona Corcoran, was appalled at the thought that the Soviets had used their own people as ‘human guinea pigs’. Walking in the area of the test site, she found that radiation levels were still 400 times above normal.</p>
	<p class="last-paragraph flow-text text-justify">This tragedy has compelled her into trying to help Kazak children of all backgrounds and disabilities condemned to life in filthy and dilapidated state institutions where staff are given few resources and rely heavily on outside help from wherever it comes. Take a look at life inside an orphanage transformed by the only Irish charity working in this most difficult country.</p>
