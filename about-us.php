			The Chernobyl Desaster
		</amp-fit-text>

		<amp-img src="./img/about-us/history.jpg" width="590" height="265" layout="responsive" class="image-center"></amp-img>
	
		<p class="flow-text text-justify">
			The Chernobyl disaster began during a systems test on <b>26 April 1986</b> at reactor number four of the Chernobyl plant, which is near Pripyat and in proximity to the administrative border with Belarus and the Dnieper River. There was a sudden and unexpected power surge, and when an emergency shutdown was attempted, a much larger spike in power output occurred, which led to a reactor vessel rupture and a series of steam explosions. These events exposed the graphite moderator of the reactor to air, causing it to ignite. The resulting fire sent week long plumes of highly radioactive fallout into the atmosphere over an extensive geographical area, including Pripyat. The plumes drifted over large parts of the western Soviet Union and Europe. According to official post-Soviet data, about 60% of the fallout landed in Belarus.
		</p>

		<p class="flow-text text-justify">
			36 hours after the accident Soviet officials enacted a 10-kilometre exclusion zone which resulted in the rapid evacuation of <b>49,000 people</b> and their animals, primarily from the largest population centre near the reactor, the town of Pripyat. Although not communicated at the time, an immediate evacuation of the town following the accident was not advisable as the road leading out of the town had heavy nuclear fallout "hotspots" deposited on it, while up to around that point, the town itself was comparatively safe due to the more favourable wind direction, permitting shelter in place to be the best safety measure to take for the town, before the winds began to change direction.
		</p>

		<p class="flow-text text-justify">
			As the plumes and subsequent fallout continued to be generated, the evacuation zone was increased from <b>10 to 30 km</b> about one week after the accident, resulting in a further <b>68,000 people</b> evacuated, including from the town of Chernobyl itself. The surveying and detection of isolated fallout hotspots outside this zone over the following year eventually resulted in <b>135,000 "long-term evacuees"</b> in total, accepting to be moved.
		</p>

		<p class="flow-text text-justify">
			The near tripling in the total number of permanently resettled to some <b>350,000 over the decades, 1986 to 2000</b>, from the most severely "contaminated" areas, is regarded as largely political in nature, with the majority of the rest evacuated in an effort to redeem loss in trust in the government, which was most common around 1990.
		</p>

		<p class="flow-text text-justify">
			The accident raised the already heightened concerns about fission reactors worldwide, and while most concern was focused on those of the same unusual design, hundreds of disparate electric-power reactor proposals, including those under construction at Chernobyl, reactor No.5 and 6, were eventually cancelled. With the worldwide issue generally being due to the ballooning in costs for new nuclear reactor safety system standards and the legal costs in dealing with the increasingly hostile/anxious public opinion. There was a precipitous drop in the prior rate of new "startups", after 1986.
		</p>
		
