			<p class="flow-text text-justify">In 2010, Fiona Corcoran visited the hospice in Ivanskoye, 200 miles northeast of Moscow. Here elderly inmates existed in shocking conditions without even basic hygiene or facilities. The hospice was in such a dreadful state that the Government shut it down, condemning the patients to the prospect of a slow and painful death. The only ambulance was withdrawn and with no other way to bring them to hospital, there were several cases of old people being found dead in their beds weeks after they had succumbed to the freezing conditions and left with nothing to eat. Doctors in the area have been forced to walk up to 20 km. a day to visit their patients. Fiona hoped to open the hospice as a secure and liveable residence for up to twenty-five people. This renovation project aimed to replace antique toilets and plumbing, provide new showers, proper dormitories, a kitchen and laundry.</p>
